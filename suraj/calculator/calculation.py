"""
Calculation module comes with basic arthmatic operations such as sum, minus, multiply, division, modulus etc
"""


def add(num1:int, num2:int) :  
    '''
    returns sum of num1, num
    '''
    return (num1+num2)

def minus(num1:int, num2:int) :  
    '''
    returns minus of num1, num
    '''
    return (num1-num2)

def multiply(num1:int, num2:int) :  
    '''
    returns multiple of num1, num
    '''
    return (num1*num2)

def division(num1:int, num2:int) :  
    '''
    returns division of num1, num
    '''
    return (num1/num2)

def modulus(num1:int, num2:int) :  
    '''
    returns division of num1, num
    '''
    return (num1%num2)



